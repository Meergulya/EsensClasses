package MethodOverloading;

public class CalculateDoubleMethodOverload {

    public static void main(String[] args) {
        int a =45;
        calculator(a);
    }


    public static void calculator(double d) {
        System.out.println(" Using double");
    }

    public static void calculator (float f) {
        System.out.println(" Using float");
    }

















}
